from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parents[1]
EXPERIMENTS_ROOT = PROJECT_ROOT / "experiments"

CLIP_BASELINE_DIR = EXPERIMENTS_ROOT / "01_clip_baseline"
CLIP_BASELINE_VISUALIZATIONS_DIR = CLIP_BASELINE_DIR / "visualizations"

STYLE_RERANKING_DIR = EXPERIMENTS_ROOT / "02_style_reranking"
STYLE_RERANKING_VISUALIZATIONS_DIR = STYLE_RERANKING_DIR / "visualizations"

QDRANT_BACKEND_DIR = EXPERIMENTS_ROOT / "03_qdrant_backend"
QDRANT_BACKEND_VISUALIZATIONS_DIR = QDRANT_BACKEND_DIR / "visualizations"

FILTERED_RETRIEVAL_DIR = EXPERIMENTS_ROOT / "04_filtered_retrieval"
FILTERED_RETRIEVAL_VISUALIZATIONS_DIR = FILTERED_RETRIEVAL_DIR / "visualizations"

FINAL_REPORT_DIR = EXPERIMENTS_ROOT / "final_report"
FINAL_REPORT_SELECTED_VISUALIZATIONS_DIR = FINAL_REPORT_DIR / "selected_visualizations"

EXPERIMENT_SCRIPTS_DIR = EXPERIMENTS_ROOT / "scripts"


def ensure_experiment_directories() -> None:
    for path in (
        CLIP_BASELINE_DIR,
        CLIP_BASELINE_VISUALIZATIONS_DIR,
        STYLE_RERANKING_DIR,
        STYLE_RERANKING_VISUALIZATIONS_DIR,
        QDRANT_BACKEND_DIR,
        QDRANT_BACKEND_VISUALIZATIONS_DIR,
        FILTERED_RETRIEVAL_DIR,
        FILTERED_RETRIEVAL_VISUALIZATIONS_DIR,
        FINAL_REPORT_DIR,
        FINAL_REPORT_SELECTED_VISUALIZATIONS_DIR,
        EXPERIMENT_SCRIPTS_DIR,
    ):
        path.mkdir(parents=True, exist_ok=True)
